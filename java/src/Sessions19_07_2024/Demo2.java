// WAP to display all the even numbers with in 1 to 10

public class Demo2 {
	
	public static void main(String[] args) {
		
		
		System.out.println("Even numbers with in 1 to 10");
		for(int count =1; count<=10; count++)
		{
			if(count%2==0)
			{
				System.out.println(count);
			}
		}
		
	}
	

}
