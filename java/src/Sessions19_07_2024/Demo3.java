//WAP to display the odd numbers with in 1 to 10


public class Demo3 {

	public static void main(String[] args) {
		
		System.out.println("odd numbers with in 1 to 10");
		
		for(int count=10; count>=1; count--)
		{
			if(count%2!=0)
			{
				System.out.println(count);
			}
		}
		
	}
}
