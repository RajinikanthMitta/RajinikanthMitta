// WAP to to find the sum of following series
//sum= 1+2+3+4+5=.........+100


public class Demo5 {
	public static void main(String[] args) {
		int sum =0;
		for(int i=1; i<=100; i++)
		{
			sum= sum+i;
			
		}
		{
			System.out.println(sum);
		}
	}

}
